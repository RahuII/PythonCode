l1 = [1, 2, 5, 75, 74, 66, 35, 6, 33]
l1.sort()  # sorts the list
print(l1)
l1.reverse()  # reverse the list
print(l1)
l1.append(45)  # adds 45 at the end of the list
print(l1)
l1.insert(0, 155)  # inserts 155 at index 0
print(l1)
l1.insert(3, 200)  # inserts 200 at index 3
print(l1)
l1.pop(2) # removes element at index 2
print(l1)
l1.remove(75) #removes 75 from the list
print(l1)
